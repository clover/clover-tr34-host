package com.clover.tr34;

public final class Tr34Exception extends RuntimeException {

    public Tr34Exception() {
        super();
    }

    public Tr34Exception(String message) {
        super(message);
    }

    public Tr34Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public Tr34Exception(Throwable cause) {
        super(cause);
    }

}
